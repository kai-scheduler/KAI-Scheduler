 SPDX-License-Identifier: Apache-2.0
 Generated by docs/presentations/build-index.sh — do not edit by hand.
-->
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>KAI Scheduler — Design Presentations</title>
  <style>
    :root {
      --bg:       #0d1117;
      --surface:  #161b22;
      --surface2: #21262d;
      --border:   #30363d;
      --text:     #e6edf3;
      --muted:    #8b949e;
      --accent:   #58a6ff;
      --radius:   6px;
    }
    *, *::before, *::after { box-sizing: border-box; margin: 0; padding: 0; }
    body {
      background: var(--bg);
      color: var(--text);
      font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Helvetica, Arial, sans-serif;
      font-size: 14px;
      line-height: 1.5;
      min-height: 100vh;
    }
    .page-header {
      background: var(--surface);
      border-bottom: 1px solid var(--border);
      padding: 16px 24px;
    }
    .page-header h1 { font-size: 18px; font-weight: 600; }
    .page-header p { color: var(--muted); font-size: 13px; margin-top: 4px; }
    main { max-width: 860px; margin: 0 auto; padding: 24px; }
    ul { list-style: none; display: grid; gap: 12px; }
    .card {
      display: flex;
      flex-direction: column;
      gap: 4px;
      background: var(--surface);
      border: 1px solid var(--border);
      border-radius: var(--radius);
      padding: 16px 18px;
      text-decoration: none;
      color: var(--text);
      transition: border-color .15s ease, background .15s ease;
    }
    .card:hover { border-color: var(--accent); background: var(--surface2); }
    .card-title { font-size: 15px; font-weight: 600; color: var(--accent); }
    .card-file { font-size: 12px; color: var(--muted); font-family: ui-monospace, SFMono-Regular, Menlo, monospace; }
    .empty {
      color: var(--muted);
      background: var(--surface);
      border: 1px dashed var(--border);
      border-radius: var(--radius);
      padding: 24px;
      text-align: center;
    }
    .empty code { color: var(--text); }
  </style>
</head>
<body>
  <header class="page-header">
    <h1>KAI Scheduler — Design Presentations</h1>
    <p>Archived presentations of KAI Scheduler design documents.</p>
  </header>
  <main>
    <ul>
${body}
    </ul>
  </main>
</body>
</html>
EOF

echo "Wrote $OUT_DIR/index.html ($count presentation(s))."
