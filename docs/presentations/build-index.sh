# Assembles the publishable presentations site into an output directory:
#   - copies every *.html / *.css from this folder into the output dir
#   - generates index.html linking each presentation, using its <title> as link text
#
# Repo-only files (this script, README.md) are never copied, so they don't reach the site.
#
# Usage: build-index.sh [output-dir]   (default: ./_site)

set -euo pipefail

SRC_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
OUT_DIR="${1:-_site}"

mkdir -p "$OUT_DIR"
shopt -s nullglob

# Copy presentation assets (html + css) into the output dir.
for f in "$SRC_DIR"/*.html "$SRC_DIR"/*.css; do
  [ "$(basename "$f")" = "index.html" ] && continue
  cp "$f" "$OUT_DIR"/
done

# Extract the first <title>…</title> text from an HTML file (fallback: filename).
extract_title() {
  local file="$1" title
  title="$(tr '\n' ' ' < "$file" \
    | grep -oiE '<title>[^<]*</title>' \
    | head -n1 \
    | sed -E 's/<[^>]+>//g' \
    | sed -E 's/^[[:space:]]+//; s/[[:space:]]+$//')"
  if [ -z "$title" ]; then
    title="$(basename "$file" .html)"
  fi
  printf '%s' "$title"
}

# HTML-escape text for safe interpolation into the generated page.
escape() {
  printf '%s' "$1" \
    | sed -e 's/&/\&amp;/g' -e 's/</\&lt;/g' -e 's/>/\&gt;/g' -e 's/"/\&quot;/g'
}

# Build the list of presentation cards.
cards=""
count=0
for f in "$SRC_DIR"/*.html; do
  name="$(basename "$f")"
  [ "$name" = "index.html" ] && continue
  title="$(escape "$(extract_title "$f")")"
  cards="${cards}      <li><a class=\"card\" href=\"${name}\"><span class=\"card-title\">${title}</span><span class=\"card-file\">${name}</span></a></li>
"
  count=$((count + 1))
done

if [ "$count" -eq 0 ]; then
  body='      <li class="empty">No presentations yet. Add an HTML file to <code>docs/presentations/</code> to see it listed here.</li>'
else
  body="$cards"
fi

cat > "$OUT_DIR/index.html" <<EOF
<!DOCTYPE html>
<!--
